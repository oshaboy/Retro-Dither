                if y < y_lim - 1:
                    pixel[x, y + 1] = self.enhance_calc(pixel[x, y + 1], error, ErrorToBottom)

                if x < x_lim - 1 and y < y_lim - 1:
                    pixel[x + 1, y + 1] = self.enhance_calc(pixel[x+1, y + 1], error, ErrorToBottomRight)

